const API_KEY = "d86a0de9";
const URL = `http://www.omdbapi.com/?apikey=${API_KEY}&s=`;

const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState(tempWatchedData);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState("");
  const query = "spiderman";

  const fetchMovies = async () => {
    try {
      setLoading(true);
      const res = await fetch(`${URL}${query}sawe`);
      // console.log("res -> ", res);
      if (!res.ok)
        throw new Error("Something went wrong while fetching Movies Data");
      const data = await res.json();
      // console.log(data);
      if (data.Response === "False")
        throw new Error("Movies Not Found with this query");
      setMovies(data.Search);
    } catch (err) {
      console.error(err.message);
      console.error(err);
      setError(err.message);
    } finally {
      setLoading(false);
    }
  };

  useEffect(() => {
    fetchMovies();
  }, []);

  return (
    <>
      <NavBar>
        <Logo />
        <Input />
        <NumResults movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {loading && <Loader />}
          {!loading && !error && <MoviesList movies={movies} />}
          {error && <ErrorComp message={error} />}
        </Box>
        <Box>
          <WatchedSummary watched={watched} />
          <WatchedMoviesList watched={watched} />
        </Box>
      </Main>
    </>
  );
}

